/// JeepneyGo spacing system
/// Based on 8dp grid system
class AppSpacing {
  AppSpacing._();

  // ============ BASE SPACING ============
  /// 4dp - Extra small
  static const double xs = 4;

  /// 8dp - Small
  static const double sm = 8;

  /// 12dp - Medium small
  static const double md = 12;

  /// 16dp - Medium (default)
  static const double lg = 16;

  /// 24dp - Large
  static const double xl = 24;

  /// 32dp - Extra large
  static const double xxl = 32;

  /// 48dp - Extra extra large
  static const double xxxl = 48;

  // ============ PADDING ============
  /// Default screen padding
  static const double screenPadding = 16;

  /// Card content padding
  static const double cardPadding = 16;

  /// Button padding
  static const double buttonPaddingHorizontal = 24;
  static const double buttonPaddingVertical = 16;

  // ============ GAPS ============
  /// Gap between list items
  static const double listItemGap = 12;

  /// Gap between sections
  static const double sectionGap = 24;

  /// Gap between form fields
  static const double formFieldGap = 16;

  // ============ BORDER RADIUS ============
  /// Small radius (chips, small buttons)
  static const double radiusSm = 8;

  /// Medium radius (cards, inputs)
  static const double radiusMd = 12;

  /// Large radius (dialogs, bottom sheets)
  static const double radiusLg = 16;

  /// Extra large radius (modals)
  static const double radiusXl = 20;

  /// Full radius (circular)
  static const double radiusFull = 999;

  // ============ ICON SIZES ============
  /// Small icon (inline text)
  static const double iconSm = 16;

  /// Medium icon (default)
  static const double iconMd = 24;

  /// Large icon (prominent actions)
  static const double iconLg = 32;

  /// Extra large icon (empty states)
  static const double iconXl = 48;

  // ============ COMPONENT HEIGHTS ============
  /// Standard button height
  static const double buttonHeight = 56;

  /// App bar height
  static const double appBarHeight = 56;

  /// Bottom nav height
  static const double bottomNavHeight = 64;

  /// List tile height
  static const double listTileHeight = 72;

  /// Map control button size
  static const double mapButtonSize = 48;
}
